function [f] = three_body_equations(x)

    G = 1;
    m1 = .5;
    m2 = .5;
    m3 = .5;
    %x = [x1 y1 z1 x1' y1' z1' x2 y2 z2 x2' y2' z2' x3 y3 z3 x3' y3' z3'] 
    %12 1st order ODEs for (x,y,z) of 2 masses
    %x1'
    f(1) = x(4);
    %y1'
    f(2) = x(5);
    %z1'
    f(3) = x(6);
    %x1''
    f(4) = -G*(m2*(x(1)-x(7))/((x(1)-x(7))^2 + (x(2)-x(8))^2 + (x(3)-x(9))^2)^(3/2) + m3*((x(1)-x(13))/((x(1)-x(13))^2 + (x(2)-x(14))^2 + (x(3)-x(15))^2)^(3/2)));
    %y1''
    f(5) = -G*(m2*(x(2)-x(8))/((x(1)-x(7))^2 + (x(2)-x(8))^2 + (x(3)-x(9))^2)^(3/2) + m3*((x(2)-x(14))/((x(1)-x(13))^2 + (x(2)-x(14))^2 + (x(3)-x(15))^2)^(3/2)));
    %z1''
    f(6) = -G*(m2*(x(3)-x(9))/((x(1)-x(7))^2 + (x(2)-x(8))^2 + (x(3)-x(9))^2)^(3/2) + m3*((x(3)-x(15))/((x(1)-x(13))^2 + (x(2)-x(14))^2 + (x(3)-x(15))^2)^(3/2)));
    %x2'
    f(7) = x(10);
    %y2'
    f(8) = x(11);
    %z2'
    f(9) = x(12);
    %x2''
    f(10) = -G*(m1*(x(7)-x(1))/((x(7)-x(1))^2 + (x(8)-x(2))^2 + (x(9)-x(3))^2)^(3/2) + m3*((x(7)-x(13))/((x(7)-x(13))^2 + (x(8)-x(14))^2 + (x(9)-x(15))^2)^(3/2)));
    %y2''
    f(11) = -G*(m1*(x(8)-x(2))/((x(7)-x(1))^2 + (x(8)-x(2))^2 + (x(9)-x(3))^2)^(3/2) + m3*((x(8)-x(14))/((x(7)-x(13))^2 + (x(8)-x(14))^2 + (x(9)-x(15))^2)^(3/2)));
    %z2''
    f(12) = -G*(m1*(x(9)-x(3))/((x(7)-x(1))^2 + (x(8)-x(2))^2 + (x(9)-x(3))^2)^(3/2) + m3*((x(9)-x(15))/((x(7)-x(13))^2 + (x(8)-x(14))^2 + (x(9)-x(15))^2)^(3/2)));
    %x3'
    f(13) = x(16);
    %y3'
    f(14) = x(17);
    %z3'
    f(15) = x(18);
    %x3''
    f(16) = -G*(m1*(x(13)-x(1))/((x(13)-x(1))^2 + (x(14)-x(2))^2 + (x(15)-x(3))^2)^(3/2) + m2*((x(13)-x(7))/((x(13)-x(7))^2 + (x(14)-x(8))^2 + (x(15)-x(9))^2)^(3/2)));
    %y3''
    f(17) = -G*(m1*(x(14)-x(2))/((x(13)-x(1))^2 + (x(14)-x(2))^2 + (x(15)-x(3))^2)^(3/2) + m2*((x(14)-x(8))/((x(13)-x(7))^2 + (x(14)-x(8))^2 + (x(15)-x(9))^2)^(3/2)));
    %z3''
    f(18) = -G*(m1*(x(15)-x(3))/((x(13)-x(1))^2 + (x(14)-x(2))^2 + (x(15)-x(3))^2)^(3/2) + m2*((x(15)-x(9))/((x(13)-x(7))^2 + (x(14)-x(8))^2 + (x(15)-x(9))^2)^(3/2)));
end 
